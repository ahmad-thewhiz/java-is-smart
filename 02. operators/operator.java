public class operator {
    public static void main(String args[]) {
        // increment and decrement operator
        int a = 10;
        System.out.println("a++ = " + a++);
        System.out.println("a = " + a);
        System.out.println("++a = " + ++a);
        System.out.println("a = " + a);
        System.out.println("a-- = " + a--);
        System.out.println("a = " + a);
        System.out.println("--a = " + --a);
        System.out.println("a = " + a);
    }
}
